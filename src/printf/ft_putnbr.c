#include "../../inc/ft_printf.h"

ssize_t	ft_putnbr(int n)
{
	ssize_t	i;

	if (n == -2147483648)
		return (ft_putstr("-2147483648"));
	i = 0;
	if (n < 0)
	{
		if (ft_putchar('-') < 0)
			return (-1);
		n = -n;
		i++;
	}
	if (n >= 10)
	{
		i += ft_putnbr(n / 10);
		if (i < 0)
			return (-1);
	}
	if (ft_putchar("0123456789"[n % 10]) < 0)
		return (-1);
	i++;
	return (i);
}
