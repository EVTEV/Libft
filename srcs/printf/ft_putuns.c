#include "../../inc/ft_printf.h"

int	ft_putuns(unsigned int n)
{
	ssize_t	i;

	i = 0;
	if (n > 9)
	{
		i += ft_putuns(n / 10);
		if (i < 0)
			return (-1);
	}
	if (ft_putchar("0123456789"[n % 10]) < 0)
		return (-1);
	i++;
	return (i);
}
